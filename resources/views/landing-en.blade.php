<h2>{{ trans('app.name') }}</h2>
<p>Idea environment is a place for gathering ideas on how to make Swedbank better, both from the customer and the employee perspective. Anyone can add their ideas and doing so, help shape the possible direction that Swedbank as a whole is heading to.</p>

<ul class="features-list">
  <li>
    <img class="feature-icon" src="images/light-bulb.svg"/>
    <h3>What kind of ideas are allowed?</h3>
    <p>We definitely don't want to limit Your fantasy in any way &ndash; be as crazy as You want! Just try to remember that bank related ideas have a better chance of getting done.</p>
  </li>
  <li>
    <img class="feature-icon" src="images/rocket.svg"/>
    <h3>What happens after I submit my idea?</h3>
    <p>Your idea will be visible to Your colleagues, who can then provide You feedback, either by voting or commenting.</p>
  </li>
  <li>
    <img class="feature-icon" src="images/people.svg"/>
    <h3>What's the purpose of voting?</h3>
    <p>Voting helps us distinguish popular ideas that have more perspective. For ideas of digital nature, a hackathon happens at least once a year to try and make more popular ideas come to life.</p>
  </li>
</ul>

